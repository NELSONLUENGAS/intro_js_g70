/**
 * ***Tipos de datos****
 * 
 * number ==> 1, 2, 3.5 -3, 0
 * boolean ==> true or false   también son representados con números true = 1 o cuaquier positivo mayor a 1 or false = 0
 * string ==> "Hola pepito", '@gmail.com' también puedo concatenar
 * 
 * ***Concatenar strings***
 * respuesta = 4
 * 
 * "La respuesta correcta es: " + respuesta   ==> "La respuesta correcta es: 4"
 * 
 *  "¿"  +  " Hola cómo estás "  +  "?"  ==>  "¿ Hola cómo estás ?"
 * 
 * 
 * ***Interpolación***
 * Usaremos un placeholder ==> ${}
 * respuesta = 4
 * 
 * `La respuesta correcta es: ${respuesta}`  ==> "La respuesta correcta es: 4"
 * 
 * Object
 * {} ==> object
 * [] ==> array
 * 
 */



/**
 * VARIABLES == 🗳️
 * 
 * respuesta ==> declarar
 * respuesta = 4;  ==>   declarar y asignar una variable
 * 
 * 
 * Palabras reservadas
 * var  ==> permite definir una variable pública / pueden tener el mismo nombre / puede cambiar
 * let ==> permite definir una variable privada / no se pueden tener el mismo nombre / puede cambiar
 * 
 * const ==> Debe ser declarada e inicializada / no se pueden modificar / deben ser únicas de nombre
 */

/**
 * OPERADORES
 * 
 *****asignación**** 

 * incremento ==> ++   / Incrementa en 1  // var contador++
 * decremento ==> --  / Decrementar en -1  // var contador--
 * 
 * 
 *mas igual (+=)/  var contador += 2
 *menos igual (-=) /  var contador -= 1
 * 
 *igual (=) / var contador = 1
 * 
 * COMPARACIÓN
 * igual (==)  ==>> 1 == 1 /devuelve un boleano ==> true
 * igual estrícto (===)  ==>> 1 === '1' /devuelve un boleano ==> false
 * 
 * diferente (!=)  ==>>  1 != 1  /devuelve un boleano ==> false
 * diferente estrícto (!==)  ==>> 1 !== '1' /devuelve un boleano ==> true
 * 
 * mayor (>) ==>>  1 > 2  /devuelve un boleano ==> false
 * mayor Igual (>=) 1 >= 1  /devuelve un boleano ==> true
 * 
 * menor (<)  1 < 2  /devuelve un boleano ==> true
 * menor igual (<=) 1 <= 1  /devuelve un boleano ==> true
 * 
 * 
 * not (!) negarlo convertirlo en lo contrario ==> !true = false  / !false = true
 */
